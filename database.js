    // Методы для работы с данными
    getCategoryList() {
        return Object.values(this.categories).map(cat => ({
            id: cat.id,
            title: cat.title,
            image: cat.image
        }));
    },

    getCategorySituations(categoryId) {
        return this.categories[categoryId]?.situations || ["Категория не найдена"];
    },

    getCategoryColor(categoryId) {
        return this.categories[categoryId]?.color || '#ffffff';
    },

    getRandomSituation() {
        const allSituations = [];
        Object.values(this.categories).forEach(category => {
            category.situations.forEach(text => {
                allSituations.push({
                    text,
                    category: category.title,
                    categoryId: category.id
                });
            });
        });
        return allSituations.length > 0 
            ? allSituations[Math.floor(Math.random() * allSituations.length)]
            : { text: "Попробуйте выбрать категорию", category: "Ошибка", categoryId: 0 };
    }
